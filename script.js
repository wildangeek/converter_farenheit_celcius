// Celcius to farenheit
function convertToFarenheit(celsius) {
    let fahrenheit = celsius * 9/5 + 32
    return fahrenheit;
  }
  
  convertToFarenheit(88);

//Farenheit to celcius
function convertToCelcius(farenheit) {
    let celcius = farenheit * 9/5 + 32
    return celcius;
  }
  
  convertToCelcius(11);